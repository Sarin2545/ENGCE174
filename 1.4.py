# List initialization
number = [1, 2, 3, 4, 5]

# Accessing elements
print("First element:", number[0]) # Output: 1
print("Last element:", number[-1]) # Output: 5

# Slicing
print("Sliced element:", number[2:4]) # Output: [3, 4]

# Appending and extending
number.append(6)
print("After append:", number) # Output: [1, 2, 3, 4, 5, 6]

# Removing elements
number.remove(3)
print("After removal:", number) # Output: [1, 2, 4, 5, 6]